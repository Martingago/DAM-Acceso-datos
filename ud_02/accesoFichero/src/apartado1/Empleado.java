package apartado1;

public class Empleado{

    int identificador;
    String nombre;
    String direccion;
    float salario;
    float comision;

    public Empleado(int identificador, String nombre, String direccion, float salario, float comision) {
        this.identificador = identificador;
        this.nombre = nombre;
        this.direccion = direccion;
        this.salario = salario;
        this.comision = comision;
    }

}
